#include "libft.h"

void	ft_removeend(char **out, int i)
{
	while (i != 0 && ((*out)[i] == ' ' || (*out)[i] == '\t'
				|| (*out)[i] == '\n'))
		i--;
	(*out)[i + 1] = '\0';
}
